-------------------------------------------------------------------------------------

The Throwable Chairs component allows players to pick up a chair and throw it at other
players, causing them to be killed. This adds a fun component to social spaces.
-----------------------------------------------------------------------------------------

==========
How to use
==========

Drag the Throwable Chairs template into the Hierarchy. Enter preview mode and pick up a chair
to throw it around.

More chairs can be added by opening up the folder and creating more Chair templates.

